    {
        $posts = DB::table('users')
            ->join('posts', 'users.id', '=', 'posts.user_id')
            ->where('id', 'LIKE', '%' . Auth::user()->id . '%')
            ->get();

        return view('pages.user.index', ['posts' => $posts]);
